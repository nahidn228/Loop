//incremental
console.log("incremental value of 0 to 10");
for (i = 0; i <= 10; i++) {
  console.log(i);
}

//decremental
console.log("decremental value of 10 to 0");
for (j = 10; j >= 0; j--) {
  console.log(j);
}

console.log("decremental value of 20 to 11");
for (k = 20; k >= 11; k--) {
  console.log(k);
}
