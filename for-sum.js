//use for loop
let sum = 0;

for (i = 11; i <= 20; i++) {
  sum = sum + i;
  console.log(i);
}
console.log("sum of the numbers from 11 to 20 is = ", sum);

//use while loop
console.log("using while loop find the value of sum 11 to 20")

let j = 11;
let summ = 0;
while (j <= 20) {
  console.log(j);
  summ = summ + j;
  j = j + 1;
}
console.log( "sum of the numbers from 11 to 20 is = ", summ);
